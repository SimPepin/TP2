import java.util.Date;


public class Date {
	
	
	int année = 0;
	int mois = 0;
	int jour = 0;
	int heure = 0;
	int minute = 0;

	public Date(int année, int mois, int jour, int heure, int minute) {
		
		this.année = année;
		this.mois = mois;
		this.jour = jour;
		this.heure = heure;
		this.minute = minute;
		
	}

	
	
	
}